DROP TABLE IF EXISTS `Album` CASCADE;
DROP TABLE IF EXISTS `Artist` CASCADE;
DROP TABLE IF EXISTS `Tracks` CASCADE;
DROP TABLE IF EXISTS `Genre` CASCADE;
DROP TABLE IF EXISTS `Playlist` CASCADE;

CREATE TABLE `Album` (

id INTEGER GENERATED BY DEFAULT AS INDENTITY,
name VARCHAR(100) NOT NULL,
fk_artistid INTEGER,
fk_genreid INTEGER,
PRIMARY KEY (id)
					 );
					 
CREATE TABLE `Artist` (

id INTEGER GENERATED BY DEFAULT AS INDENTITY,
name VARCHAR(100) NOT NULL,
PRIMARY KEY (id)
					 );

CREATE TABLE `Track` (

id INTEGER GENERATED BY DEFAULT AS INDENTITY,
name VARCHAR(100) NOT NULL,
duration INTEGER,
lyrics VARCHAR (1000),
fk_albumid INTEGER,
fk_playlistid INTEGER,
PRIMARY KEY (id)
					 );
					 
CREATE TABLE `Genre` (

id INTEGER GENERATED BY DEFAULT AS INDENTITY,
name VARCHAR(100) NOT NULL,
description VARCHAR(250),
PRIMARY KEY (id)
					 );

CREATE TABLE `Playlist` (

id INTEGER GENERATED BY DEFAULT AS INDENTITY,
name VARCHAR(100) NOT NULL,
description VARCHAR(500),
artwork VARCHAR(1000),
PRIMARY KEY (id)
					 );					 